# data types = типы данных

# 1. int — целые числа (номера)
# Пример: 5, -12, 1000
# Используется для счёта, индексов, любых целых значений.

# 2. float — числа с плавающей точкой (десятичные)
# Пример: 3.14, -0.5, 0.001
# Для измерений, вычислений с дробями.

# 3. complex — комплексные числа
# Пример: 2 + 3j, -1j
# Используется в математике и инженерии.

# 4. bool — логический тип
# Пример: True, False
# Для условий, проверок, логики.

# 5. str — строки (текст)
# Пример: "Hello", 'Python'
# Для текста, слов, сообщений.

# 6. list — список
# Пример: [1, 2, 3], ["a", "b"]
# Можно хранить несколько значений, изменяемый.

# 7. tuple — кортеж
# Пример: (1, 2, 3), ("a", "b")
# Список, но неизменяемый.

# 8. range — диапазон чисел
# Пример: range(0, 5) → 0,1,2,3,4
# Для циклов, генерации последовательностей.

# 9. set — множество
# Пример: {1, 2, 3}
# Хранит уникальные значения, порядок не важен.

# 10. frozenset — неизменяемое множество
# Пример: frozenset([1, 2, 3])
# Как set, но нельзя менять.

# 11. dict — словарь
# Пример: {"a": 1, "b": 2}
# Хранит данные в виде ключ → значение.

# 12. bytes — байты
# Пример: b"hello"
# Для работы с файлами, сетями, бинарными данными.

# 13. bytearray — изменяемые байты
# Пример: bytearray([65, 66, 67])
# Можно изменять отдельные элементы байтов.

# 14. memoryview — представление памяти
# Пример: memoryview(b"abc")
# Для быстрого доступа к данным в памяти без копирования.

# 15. NoneType — отсутствие значения
# Пример: None
# Обозначает пустоту, отсутствие данных.

# Неизменяемые типы (Immutable)
# int → 5 (нельзя изменить 5 на 6, создаётся новое число)
# float → 3.14
# complex → 2 + 3j
# bool → True / False
# str → "Hello"
# tuple → (1, 2, 3)
# frozenset → frozenset([1, 2, 3])
# bytes → b"hello"
# range → range(0, 5)
# NoneType → None

# Изменяемые типы (Mutable)
# list → [1, 2, 3] (можно добавлять, удалять, менять элементы)
# set → {1, 2, 3}
# dict → {"a": 1, "b": 2}
# bytearray → bytearray([65, 66, 67])
# memoryview → memoryview(b"abc")

# Immutable — безопаснее для хранения значений, которые не должны меняться.
# Mutable — удобно, когда нужно динамически изменять данные.

# type покажет какой тип данный
