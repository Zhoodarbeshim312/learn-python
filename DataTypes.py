# Типы данных в Python

# 1. int — целые числа (номера)
# Пример: 5, -12, 1000
# Используется для счёта, индексов, любых целых значений.

# 2. float — числа с плавающей точкой (десятичные)
# Пример: 3.14, -0.5, 0.001
# Для измерений, вычислений с дробями.

# 3. complex — комплексные числа
# Пример: 2 + 3j, -1j
# Используется в математике и инженерии.

# 4. bool — логический тип
# Пример: True, False
# Для условий, проверок, логики.

# 5. str — строки (текст)
# Пример: "Hello", 'Python'
# Для текста, слов, сообщений.

# 6. list — список
# Пример: [1, 2, 3], ["a", "b"]
# Можно хранить несколько значений, изменяемый.

# 7. tuple — кортеж
# Пример: (1, 2, 3), ("a", "b")
# Список, но неизменяемый.

# 8. range — диапазон чисел
# Пример: range(0, 5) → 0,1,2,3,4
# Для циклов, генерации последовательностей.

# 9. set — множество
# Пример: {1, 2, 3}
# Хранит уникальные значения, порядок не важен.

# 10. frozenset — неизменяемое множество
# Пример: frozenset([1, 2, 3])
# Как set, но нельзя менять.

# 11. dict — словарь
# Пример: {"a": 1, "b": 2}
# Хранит данные в виде ключ → значение.

# 12. bytes — байты
# Пример: b"hello"
# Для работы с файлами, сетями, бинарными данными.

# 13. bytearray — изменяемые байты
# Пример: bytearray([65, 66, 67])
# Можно изменять отдельные элементы байтов.

# 14. memoryview — представление памяти
# Пример: memoryview(b"abc")
# Для быстрого доступа к данным в памяти без копирования.

# 15. NoneType — отсутствие значения
# Пример: None
# Обозначает пустоту, отсутствие данных.

# 1. int — целые числа
# immutable
# Пример: 5, -12, 1000
# Используется для счёта, индексов, любых целых значений.
# x = 10
# print(type(x))  # <class 'int'>

# 2. float — числа с плавающей точкой (десятичные)
# Immutable
# Пример: 3.14, -0.5, 0.001
# Для измерений, вычислений с дробями.
# y = 2.5
# print(type(y))  # <class 'float'>

# 3. complex — комплексные числа
# Immutable
# Пример: 2 + 3j, -1j
# Используется в математике и инженерии.
# z = 1 + 2j
# print(type(z))  # <class 'complex'>

# 4. bool — логический тип
# Immutable
# Пример: True, False
# Для условий, проверок, логики.
# is_active = True
# print(type(is_active))  # <class 'bool'>

# 5. str — строки (текст)
# Immutable
# Пример: "Hello", 'Python'
# Для текста, слов, сообщений.
# name = "Актанов"
# print(type(name))  # <class 'str'>

# 6. list — список
# Mutable
# Пример: [1, 2, 3], ["a", "b"]
# Хранит несколько значений, можно менять элементы.
# numbers = [1, 2, 3]
# numbers.append(4)
# print(numbers)  # [1, 2, 3, 4]

# 7. tuple — кортеж
# Immutable
# Пример: (1, 2, 3), ("a", "b")
# Похож на список, но нельзя менять элементы.
# coords = (10, 20)
# print(type(coords))  # <class 'tuple'>

# 8. range — диапазон чисел
# Immutable
# Пример: range(0, 5) → 0,1,2,3,4
# Используется в циклах и для генерации последовательностей.
# for i in range(5):
#     print(i)

# 9. set — множество
# Mutable
# Пример: {1, 2, 3}
# Хранит только уникальные значения, порядок не важен.
# s = {1, 2, 2, 3}
# print(s)  # {1, 2, 3}

# 10. frozenset — неизменяемое множество
# Immutable
# Пример: frozenset([1, 2, 3])
# Как set, но нельзя менять элементы.

# 11. dict — словарь
# Mutable
# Пример: {"a": 1, "b": 2}
# Хранит данные в виде ключ → значение.
# person = {"name": "Жоодарбешим", "age": 20}
# person["age"] = 21
# print(person)  # {'name': 'Жоодарбешим', 'age': 21}

# 12. bytes — байты
# Immutable
# Пример: b"hello"
# Для работы с файлами, сетями, бинарными данными.
# b = b"data"
# print(type(b))  # <class 'bytes'>

# 13. bytearray — изменяемые байты
# Mutable
# Пример: bytearray([65, 66, 67])
# Можно менять отдельные элементы.

# 14. memoryview — представление памяти
# Mutable (частично)
# Пример: memoryview(b"abc")
# Для быстрого доступа к данным в памяти без копирования.

# 15. NoneType — отсутствие значения
# Immutable
# Пример: None
# Обозначает пустоту, отсутствие данных.
# x = None
# print(type(x))  # <class 'NoneType'>

# Immutable (неизменяемые)
# int, float, complex, bool, str, tuple, frozenset, bytes, range, NoneType
# Безопасно хранить данные, которые не должны меняться.

# Mutable (изменяемые)
# list, set, dict, bytearray, memoryview
# Удобно для динамических изменений данных.

# | Тип данных   | Пример               | Mutable / Immutable | Назначение                       |
# | ------------ | -------------------- | ------------------- | -------------------------------- |
# | `int`        | `5`                  | Immutable           | Целые числа, счёт                |
# | `float`      | `3.14`               | Immutable           | Дробные числа, измерения         |
# | `complex`    | `2 + 3j`             | Immutable           | Комплексные числа, математика    |
# | `bool`       | `True/false`         | Immutable           | Логические значения, условия     |
# | `str`        | `"Hello"`            | Immutable           | Текст, строки                    |
# | `list`       | `[1, 2, 3]`          | Mutable             | Списки, можно менять элементы    |
# | `tuple`      | `(1, 2, 3)`          | Immutable           | Кортежи, нельзя менять           |
# | `range`      | `range(0,5)`         | Immutable           | Последовательности чисел         |
# | `set`        | `{1,2,3}`            | Mutable             | Множества, уникальные элементы   |
# | `frozenset`  | `frozenset([1,2,3])` | Immutable           | Неизменяемое множество           |
# | `dict`       | `{"a":1}`            | Mutable             | Словарь, ключ → значение         |
# | `bytes`      | `b"hello"`           | Immutable           | Байты, файлы, сети               |
# | `bytearray`  | `bytearray([65,66])` | Mutable             | Изменяемые байты                 |
# | `memoryview` | `memoryview(b"abc")` | Mutable             | Быстрый доступ к данным в памяти |
# | `NoneType`   | `None`               | Immutable           | Отсутствие значения              |
